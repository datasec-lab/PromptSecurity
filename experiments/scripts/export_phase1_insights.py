#!/usr/bin/env python3
"""
Compute Phase1 insights and emit LaTeX-friendly plot snippets.

Outputs:
  - Latex/phase1_model_avg_plot.tex (horizontal bar chart using pgfplots)

Usage:
  python experiments/scripts/export_phase1_insights.py \
    --placeholders-dir experiments/placeholders_phase1 \
    --output-plot Latex/phase1_model_avg_plot.tex \
    --top 20
"""

from __future__ import annotations

import argparse
from pathlib import Path
from statistics import mean
from typing import Any, Dict, List
import sys

from pathlib import Path as _Path
_project_root = _Path(__file__).resolve().parents[2]
if str(_project_root) not in sys.path:
    sys.path.insert(0, str(_project_root))

from experiments.core.placeholder_dashboard import PlaceholderDashboard


def calc_model_avgs(placeholders_dir: str, status: str = "success") -> List[Dict[str, Any]]:
    dash = PlaceholderDashboard(placeholders_dir=placeholders_dir)
    dash.load_placeholders(status)

    # group placeholders by (model, dataset)
    by_model: Dict[str, Dict[str, Dict[str, Any]]] = {}
    for ph in dash.placeholders:
        cfg = ph.get("config", {})
        model = cfg.get("model", "N/A")
        dataset = cfg.get("dataset", "N/A")
        by_model.setdefault(model, {})[dataset] = ph

    rows: List[Dict[str, Any]] = []
    for model, per_ds in by_model.items():
        ds_avgs: List[float] = []
        for ds, ph in per_ds.items():
            _, avg_asr = dash._calculate_individual_judger_asrs(ph)  # type: ignore[attr-defined]
            ds_avgs.append(avg_asr)
        if not ds_avgs:
            continue
        rows.append({
            "model": model,
            "avg": mean(ds_avgs),
            "datasets": sorted(list(per_ds.keys()))
        })

    # sort desc by avg
    rows.sort(key=lambda r: -r["avg"])
    return rows


def write_pgfplots_bar(output_path: str, rows: List[Dict[str, Any]], top: int | None = None) -> None:
    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)

    if top is not None and len(rows) > top:
        rows = rows[:top]

    # Escape underscores for labels
    def esc(s: str) -> str:
        return s.replace("_", "\\_")

    models = [esc(r["model"]) for r in rows]
    coords = " ".join(f"({r['avg']:.4f},{esc(r['model'])})" for r in rows)

    height_in = f"{max(6, len(models)) * 0.35:.2f}"
    models_str = ", ".join(models)
    tikz_lines = [
        "% Auto-generated by export_phase1_insights.py",
        "% Requires \\usepackage{pgfplots} and \\pgfplotsset{compat=1.18}",
        "\\begin{tikzpicture}",
        "  \\begin{axis}[",
        "    xbar,",
        "    width=\\textwidth,",
        f"    height={height_in}in,",
        "    xmin=0, xmax=1,",
        "    symbolic y coords={ " + models_str + " },",
        "    ytick=data,",
        "    ylabel={Model}, xlabel={Avg Clean ASR},",
        "    bar width=6pt,",
        "    nodes near coords={\\pgfmathprintnumber[precision=1, fixed zerofill]{\\pgfplotspointmeta}\\%},",
        "    point meta rel=per plot,",
        "    tick label style={/pgf/number format/.cd, fixed, fixed zerofill, precision=1},",
        "    xmajorgrids,",
        "  ]",
        "    \\addplot coordinates { " + coords + " };",
        "  \\end{axis}",
        "\\end{tikzpicture}",
    ]
    out.write_text("\n".join(tikz_lines) + "\n", encoding="utf-8")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--placeholders-dir", default="experiments/placeholders_phase1")
    ap.add_argument("--status", choices=["pending", "running", "in_progress", "success", "failed"], default="success")
    ap.add_argument("--output-plot", default="Latex/phase1_model_avg_plot.tex")
    ap.add_argument("--top", type=int, default=20)
    args = ap.parse_args()

    rows = calc_model_avgs(args.placeholders_dir, status=args.status)
    write_pgfplots_bar(args.output_plot, rows, top=args.top)
    print(f"✅ Wrote plot to {args.output_plot}")


if __name__ == "__main__":
    main()
